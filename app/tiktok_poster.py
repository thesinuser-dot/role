# Errors that mean auth is broken — retrying won't help and can trigger lockouts
_AUTH_ERROR_SIGNALS = (
    "login",
    "session",
    "unauthorized",
    "not logged",
    "authentication",
    "cookie",
    "captcha",
    "verify",
    "challenge",
)


