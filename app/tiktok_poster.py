def _extract_session_id(raw: str) -> Optional[str]:
    raw = raw.strip()
    if not raw:
        return None
    if "=" in raw:
        for part in raw.replace(";", " ").split():
            if part.startswith("sessionid="):
                return part.split("=", 1)[1].strip()
        return None
    return raw


def _write_session_cookies_file(raw_cookie_string: str) -> Path:
    """
    Build a Netscape-format cookies file from a raw cookie string or bare sessionid.

    KEY FIX: TikTok needs more than just sessionid to accept uploads.
    We parse ALL key=value pairs from the cookie string and write each one,
    so tt_csrf_token, sid_guard, uid_tt, etc. are all preserved.
    Domain is written as-is from the source — never force-prefixed with a dot.
    """
    raw = raw_cookie_string.strip()
    tmp = tempfile.NamedTemporaryFile(
        suffix=".txt", prefix="tiktok_cookies_", delete=False,
    )
    lines = [
        b"# Netscape HTTP Cookie File\n",
        b"# Generated by tiktok_poster.py\n\n",
    ]

    # If it looks like a full cookie string (has key=value pairs), parse each one
    if "=" in raw and ";" in raw:
        for part in raw.split(";"):
            part = part.strip()
            if not part or "=" not in part:
                continue
            name, _, value = part.partition("=")
            name = name.strip()
            value = value.strip()
            if name:
                # Use subdomain wildcard for TikTok cookies
                line = f".tiktok.com\tTRUE\t/\tTRUE\t9999999999\t{name}\t{value}\n"
                lines.append(line.encode())
    else:
        # Bare sessionid value — write only sessionid
        session_id = raw
        line = f".tiktok.com\tTRUE\t/\tTRUE\t9999999999\tsessionid\t{session_id}\n"
        lines.append(line.encode())

    for line in lines:
        tmp.write(line)
    tmp.close()
    return Path(tmp.name)


def _build_caption(reel_url: str, views: int, likes: int, extra_tags: list[str]) -> str:
    base_tags = " ".join(f"#{t.lstrip('#')}" for t in Config.TIKTOK_HASHTAGS)
    extra     = " ".join(f"#{t.lstrip('#')}" for t in extra_tags)
    tags_line = f"{base_tags} {extra}".strip()

    template = Config.TIKTOK_CAPTION_TEMPLATE
    if template:
        caption = (
            template
            .replace("{url}", reel_url)
            .replace("{views}", f"{views:,}")
            .replace("{likes}", f"{likes:,}")
            .replace("{tags}", tags_line)
        )
    else:
        caption = f"🎬 {tags_line}"

    return caption[:2200]


def _is_auth_error(exc: Exception) -> bool:
    """Return True if the exception looks like a TikTok auth/session failure."""
    msg = str(exc).lower()
    return any(signal in msg for signal in _AUTH_ERROR_SIGNALS)


